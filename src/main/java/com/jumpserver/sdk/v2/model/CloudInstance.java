package com.jumpserver.sdk.v2.model;

public class CloudInstance {
    private String asset;
    private String asset_display;
    private String date_sync;
    private String execution;
    private String id;
    private String instance_id;
    private String region;
    private int status;
    private String status_display;
    private String task;

    public String getAsset() {
        return asset;
    }

    public void setAsset(String asset) {
        this.asset = asset;
    }

    public String getAsset_display() {
        return asset_display;
    }

    public void setAsset_display(String asset_display) {
        this.asset_display = asset_display;
    }

    public String getDate_sync() {
        return date_sync;
    }

    public void setDate_sync(String date_sync) {
        this.date_sync = date_sync;
    }

    public String getExecution() {
        return execution;
    }

    public void setExecution(String execution) {
        this.execution = execution;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getInstance_id() {
        return instance_id;
    }

    public void setInstance_id(String instance_id) {
        this.instance_id = instance_id;
    }

    public String getRegion() {
        return region;
    }

    public void setRegion(String region) {
        this.region = region;
    }

    public int getStatus() {
        return status;
    }

    public void setStatus(int status) {
        this.status = status;
    }

    public String getStatus_display() {
        return status_display;
    }

    public void setStatus_display(String status_display) {
        this.status_display = status_display;
    }

    public String getTask() {
        return task;
    }

    public void setTask(String task) {
        this.task = task;
    }
}
